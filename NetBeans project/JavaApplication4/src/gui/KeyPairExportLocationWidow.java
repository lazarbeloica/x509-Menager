    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (path.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Niste uneli lokaciju!");
            return;
        }
        if (AESEnc.isSelected()) {
            if (password.getText().trim().equals("")) {
                JOptionPane.showMessageDialog(this, "Niste uneli lozinku!");
                return;
            }
            if (password.getText().trim().length() < 8) {
                JOptionPane.showMessageDialog(this, "Lozinka mora da ima najmanje osam karaktera!");
                return;
            }
            KeyPairExporter.exportKeyPair(alias, password.getText().trim(), path.getText().trim());
        } else {
           
            KeyPairExporter.exportKeyPair(alias, path.getText().trim());
        }

        this.setVisible(false);
        parent.dispose();
        next.setVisible(true);
        this.dispose();

    }//GEN-LAST:event_jButton1ActionPerformed


