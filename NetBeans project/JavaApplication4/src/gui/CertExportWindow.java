    public CertExportWindow(JFrame parent) {
        this.parent = parent;
        initComponents();

        SignedCertList kpl = new SignedCertList();
        ArrayList<String> keysList;
        if (kpl != null) {
            keysList = kpl.getList();

            DefaultListModel model = (DefaultListModel) certList.getModel();

            while (keysList.size() > 0) {
                int i = 0;
                String el = keysList.remove(0);
                model.add(i, el);
            }
        }

        this.setVisible(true);
        this.setResizable(false);
    }

